// 后端接口1：获取所有绘本列表（供前端调用，包含故事+图片路径）
app.get('/api/books', (req, res) => {
  res.send({
    code: 200,
    message: '获取绘本列表成功',
    data: BOOKS_DATA.map(book => ({
      id: book.id,
      title: book.title,
      subtitle: book.subtitle,
      image: book.image // 返回服务器上的图片路径，前端直接显示
    }))
  });
});

// 后端接口2：获取单本绘本详情（供前端阅读页调用，包含完整故事+图片）
app.get('/api/books/:id', (req, res) => {
  const bookId = req.params.id;
  const book = BOOKS_DATA.find(b => b.id === bookId);
  if (book) {
    res.send({
      code: 200,
      message: '获取绘本详情成功',
      data: book // 返回完整数据（故事文字+服务器图片路径）
    });
  } else {
    res.send({
      code: 404,
      message: '绘本不存在',
      data: null
    });
  }
});


app.listen(60000, () => {
  console.log('服务器后端启动成功，绘本（故事+图片）已存到服务器，可调用接口：http://172.31.111.22:60000/api/books');
});